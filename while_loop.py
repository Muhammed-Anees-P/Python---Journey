# while loop 
# while loop is used to execute a block of statements repeatedly until a given condition is satisfied.
# And when the condition becomes false, the line immediately after the loop in the program is executed.

i=20
while i<=10 :
    print(i)
    i+=1
else : 
    print('End of loop......!')



rows = 5
j = 1

while j <= rows :
    print("*" * j)
    j+=1
else :
    print('executed')    